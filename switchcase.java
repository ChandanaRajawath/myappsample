public class switchcase {
    public static void main(String[] args){
        int month=6;
        switch (month){
            case 1:
            System.out.println("jan");
            break;
            case 2:
            System.out.println("feb");
            break;
            case 3:
            System.out.println("march");
            break;
            default:
                System.out.println("default");
                System.out.println("out of switch case");
        }

    }
}
